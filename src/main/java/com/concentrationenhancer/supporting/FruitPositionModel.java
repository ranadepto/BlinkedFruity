package com.concentrationenhancer.supporting;

public class FruitPositionModel
{
	double fruitPositionX, fruitPositionY;

	public FruitPositionModel(double fruitPositionX, double fruitPositionY)
	{
		this.fruitPositionX = fruitPositionX;
		this.fruitPositionY = fruitPositionY;
	}

	public double getFruitPositionX()
	{
		return fruitPositionX;
	}

	public double getFruitPositionY()
	{
		return fruitPositionY;
	}
}
